const solution = require('../checkValid');

describe('Check if Every Row and Column Contains All Numbers', () => {
  it('should be truthy', () => {
    expect(solution([[1,2],[2,1]])).toBeTruthy();
  });

  it('should be truthy', () => {
    expect(solution([[1,2,3],[3,1,2],[2,3,1]])).toBeTruthy();
  });

  it('should be falsy', () => {
    expect(solution([[1,1,2],[1,2,3],[1,2,3]])).toBeFalsy();
  });

  it('should be falsy', () => {
    expect(solution([[1,3,1],[3,1,3], [1,3,1]])).toBeFalsy();
  });

  it('should be falsy', () => {
    expect(solution([[3,2,3,2],[4,1,2,3],[2,4,3,1],[1,3,2,4]])).toBeFalsy();
  });

  it('should be false', () => {
    expect(solution([[4,5,14,8,11,14,6,2,2,12,10,9,2,7],[6,6,5,13,4,2,14,8,3,6,13,4,3,13],[11,14,4,12,1,6,7,11,9,10,8,13,5,6],[12,10,8,1,2,5,2,6,4,4,1,3,13,3],[13,3,12,9,9,4,3,12,10,9,6,1,1,14],[5,7,6,3,6,7,11,3,7,3,3,10,6,12],[10,11,7,5,3,11,8,13,12,1,5,5,12,1],[8,1,13,6,8,3,4,1,1,14,14,6,7,5],[1,9,1,14,10,8,5,4,5,11,9,11,10,10],[7,4,9,7,12,9,1,10,6,7,11,8,14,4],[2,8,11,4,7,12,12,5,13,8,2,2,4,11],[14,12,3,10,13,10,10,14,8,2,4,12,8,9],[9,13,10,2,5,13,13,7,11,13,12,14,11,8],[3,2,2,11,14,1,9,9,14,5,7,7,9,2]])).toBeFalsy();
  });

})